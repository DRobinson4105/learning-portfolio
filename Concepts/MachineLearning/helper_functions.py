def multi_class_predictions(model, features, labels, classes, device):
    torch.manual_seed(42)
    fig = plt.figure(figsize=(18, 10))
    rows, cols = 4, 8

    # Get random batch
    pred = model(features.to(device))

    for i in range(0, 32):
        img, label = features[i], labels[i]
        fig.add_subplot(rows, cols, i + 1)
        plt.imshow(img.squeeze(), cmap="gray")
        res = "Correct: " + classes[label] + "\nGuess: " + classes[torch.argmax(pred[i])]
        plt.title(res)
        plt.axis(False)

def plot_decision_boundary(model: torch.nn.Module, X: torch.Tensor, y: torch.Tensor):
    """Plots decision boundaries of model predicting on X in comparison to y.

    Source - https://madewithml.com/courses/foundations/neural-networks/ (with modifications)
    """
    # Transfer to CPU
    model.to("cpu")
    X, y = X.cpu(), y.cpu()

    # Setup prediction boundaries and grid
    x_min, x_max = X[:, 0].min() - 0.1, X[:, 0].max() + 0.1
    y_min, y_max = X[:, 1].min() - 0.1, X[:, 1].max() + 0.1
    xx, yy = np.meshgrid(np.linspace(x_min, x_max, 101), np.linspace(y_min, y_max, 101))

    # Make features
    X_to_pred_on = torch.from_numpy(np.column_stack((xx.ravel(), yy.ravel()))).float()

    # Make predictions
    model.eval()
    with torch.inference_mode():
        y_logits = model(X_to_pred_on)

    # Test for multi-class or binary and adjust logits to prediction labels
    if len(torch.unique(y)) > 2:
        y_pred = torch.softmax(y_logits, dim=1).argmax(dim=1)  # mutli-class
    else:
        y_pred = torch.round(torch.sigmoid(y_logits))  # binary

    # Reshape preds and plot
    y_pred = y_pred.reshape(xx.shape).detach().numpy()
    plt.contourf(xx, yy, y_pred, cmap=plt.colormaps['RdYlBu'], alpha=0.7)
    plt.scatter(X[:, 0], X[:, 1], c=y, s=40, cmap=plt.colormaps['RdYlBu'])
    plt.xlim(xx.min(), xx.max())
    plt.ylim(yy.min(), yy.max())


